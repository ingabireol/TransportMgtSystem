    private void registerBtnActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_registerBtnActionPerformed
        // TODO add your handling code here:
        if(plateNoTxt.getText().trim().isEmpty() || busSizeTxt.getText().trim().isEmpty() || registerDateTxt == null || registerDateTxt.getDate().toString().trim().isEmpty() || BusIdTxt.getText().trim().isEmpty() ||  BusIdTxt.getText().equalsIgnoreCase("Server Error")  ){
            JOptionPane.showMessageDialog(this, "Plsease Fill All fields","Empty Field",JOptionPane.ERROR_MESSAGE);
            resetBtnActionPerformed(evt);
        }
        else if(!busSizeTxt.getText().matches("\\d+") || plateNoTxt.getText().length() != 7) {
            JOptionPane.showMessageDialog(this, "The Bus Size is a Number \nPlateNo is of length 7\n","Invalid Field",JOptionPane.ERROR_MESSAGE);
            resetBtnActionPerformed(evt);
        }
        else
        {
          Bus bus = new Bus();
          bus.setSize(Integer.parseInt(busSizeTxt.getText()));
          bus.setBus_id(BusIdTxt.getText());
          bus.setPlate_no(plateNoTxt.getText());
            SimpleDateFormat sdf = new SimpleDateFormat("yyyy-MM-dd");
           String date = sdf.format(registerDateTxt.getDate()).toString();
          bus.setRegDate(date);
            
         
          
            DriverDao driverdao = new DriverDao();
            BusDao busdao = new BusDao();
          if(!driverdao.BusExists(BusIdTxt.getText())){
          String feedback = busdao.registerBus(bus);
          JOptionPane.showMessageDialog(this, feedback, "Result", JOptionPane.INFORMATION_MESSAGE);
          addTableRowData();
          resetBtnActionPerformed(evt);
          }
          else{
              JOptionPane.showMessageDialog(this, "The Bus Does Already Exists", "Result", JOptionPane.ERROR_MESSAGE);
          }
        }
        
    }//GEN-LAST:event_registerBtnActionPerformed

