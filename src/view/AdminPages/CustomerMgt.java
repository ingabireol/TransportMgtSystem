    private void registerBtnActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_registerBtnActionPerformed
        // TODO add your handling code here:
         Customer customer = new Customer();

    // Validate input fields before registration
    if (custIdTxt.getText().trim().isEmpty() || fnameTxt.getText().trim().isEmpty() ||
            lnameTxt.getText().trim().isEmpty() || contactTxt.getText().trim().isEmpty()) {
        JOptionPane.showMessageDialog(this, "Please Fill All Fields", "Empty Field", JOptionPane.ERROR_MESSAGE);
        return;
    }

    customer.setCust_id(custIdTxt.getText());
    customer.setCust_first_name(fnameTxt.getText());
    customer.setCust_last_name(lnameTxt.getText());
    customer.setGender(genderCombo.getSelectedItem().toString());
    customer.setCust_phone(contactTxt.getText());

    CustomerDao customerDao = new CustomerDao();
    String feedback = customerDao.RegisterCustomer(customer);

    JOptionPane.showMessageDialog(this, feedback, "Result", JOptionPane.INFORMATION_MESSAGE);
    addTableRowData();

    


    }//GEN-LAST:event_registerBtnActionPerformed

    private void updateBtnActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_updateBtnActionPerformed
        // TODO add your handling code here:
        if (fnameTxt.getText().trim().isEmpty() || lnameTxt.getText().trim().isEmpty() ||
            contactTxt.getText().trim().isEmpty() || genderCombo.getSelectedIndex() == 0) {
        JOptionPane.showMessageDialog(this, "Please Fill All Fields", "Empty Field", JOptionPane.ERROR_MESSAGE);
        resetBtnActionPerformed(evt);
        return;
    }

    // Create a Customer object and set its properties
    Customer customer = new Customer();
    customer.setCust_id(custIdTxt.getText());
    customer.setCust_first_name(fnameTxt.getText());
    customer.setCust_last_name(lnameTxt.getText());
    customer.setGender(genderCombo.getSelectedItem().toString());
    customer.setCust_phone(contactTxt.getText());

    // Create a CustomerDao object and perform the update
    CustomerDao customerDao = new CustomerDao();
    String feedback = customerDao.UpdateCustomer(customer);

    // Display the update result
    JOptionPane.showMessageDialog(this, feedback, "Result", JOptionPane.INFORMATION_MESSAGE);
    addTableRowData();

    }//GEN-LAST:event_updateBtnActionPerformed

