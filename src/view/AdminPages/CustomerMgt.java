    private void deleteBtnActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_deleteBtnActionPerformed
        // TODO add your handling code here:
         Customer customer = new Customer();
          CustomerDao jourdao = new CustomerDao();

          String id = jourdao.generate_custId();
          customer.setCust_id(custIdTxt.getText());
          if(id.equals(custIdTxt.getText())){
              JOptionPane.showMessageDialog(this, "Please First Search For the Id","Error",JOptionPane.ERROR_MESSAGE);
          }
          else{
          int  option = JOptionPane.showConfirmDialog(this, "Are you sure you want to delete this customer ?", "Confirm ", JOptionPane.YES_NO_OPTION,JOptionPane.QUESTION_MESSAGE);
          if(option == JOptionPane.YES_OPTION){
          String feedback = jourdao.deleteCustomer(customer);
          JOptionPane.showMessageDialog(this, feedback, "Result", JOptionPane.INFORMATION_MESSAGE);
          addTableRowData();
          }
          resetBtnActionPerformed(evt);
          }
    }//GEN-LAST:event_deleteBtnActionPerformed

    private void searchBtnActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_searchBtnActionPerformed
        // TODO add your handling code here:
        try {
    if (searchIdtxt.getText().trim().isEmpty()) {
        JOptionPane.showMessageDialog(this, "Please Enter Customer ID", "Empty Field", JOptionPane.ERROR_MESSAGE);
    } else {
        // Assuming CustomerDao has a method named searchCustomer
        CustomerDao customerDao = new CustomerDao();
        Customer customer = new Customer();
        customer.setCust_id(searchIdtxt.getText());

        // Perform the search
        customer = customerDao.searchCustomer(customer);

        if (customer == null) {
            JOptionPane.showMessageDialog(this, "Customer ID does not Exist", "Result", JOptionPane.INFORMATION_MESSAGE);
        } else {
            // Display the customer information in the form fields
            custIdTxt.setText(customer.getCust_id());
            fnameTxt.setText(customer.getCust_first_name());
            lnameTxt.setText(customer.getCust_last_name());
            genderCombo.setSelectedItem(customer.getGender());
            contactTxt.setText(customer.getCust_phone());
            registerBtn.setEnabled(false);

            // Optional: You can perform additional actions if needed
        }
    }
} catch (Exception ex) {
    ex.printStackTrace();
}

    }//GEN-LAST:event_searchBtnActionPerformed


