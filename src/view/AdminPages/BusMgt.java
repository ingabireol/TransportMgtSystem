    private void addTableRowData(){
        tableModel.setRowCount(0);
        BusDao busdao = new BusDao();
        List<Bus> buses = busdao.retrieveBuses();
        for(Bus thebus: buses){
            tableModel.addRow(new Object[]{
                thebus.getBus_id(),
                thebus.getPlate_no(),
                thebus.getRegDate(),
                thebus.getSize(),
            });
        }
    }

