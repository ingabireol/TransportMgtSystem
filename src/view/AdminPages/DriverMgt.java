    private void addTableRowData(){
        tableModel.setRowCount(0);
        DriverDao jourdao = new DriverDao();
        List<Driver> drivers = jourdao.retrieveDrivers();
        for(Driver thedriver: drivers){
            tableModel.addRow(new Object[]{
                thedriver.getDriver_id(),
                thedriver.getLicence_no(),
                thedriver.getGender(),
                thedriver.getF_name(),
                thedriver.getL_name(),
                thedriver.getContact_no(),
                thedriver.getSalary(),
                thedriver.getBus_id(),
            });
        }
    }

