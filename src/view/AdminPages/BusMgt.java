    private void searchBtnActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_searchBtnActionPerformed
        // TODO add your handling code here:
        try{
        if(searchIdtxt.getText().trim().isEmpty()){
             JOptionPane.showMessageDialog(this, "Please Enter Id ","Empty Filed",JOptionPane.ERROR_MESSAGE);
        }
        else if(searchIdtxt.getText().length() == 5 && searchIdtxt.getText().contains("BS")){
            Bus bus = new Bus();
//            Driver driver1 = new Driver();

            BusDao busdao = new BusDao();
            bus.setBus_id(searchIdtxt.getText());
            bus = busdao.searchBus(bus);
            if(bus == null){
                System.out.println(searchIdtxt.getText());
                JOptionPane.showMessageDialog(this, "Id does not Exist", "Result", JOptionPane.INFORMATION_MESSAGE);
            }
            else
            {
                BusIdTxt.setText(bus.getBus_id());
                plateNoTxt.setText(bus.getPlate_no());
                busSizeTxt.setText(bus.getSize().toString());
                SimpleDateFormat sdf = new SimpleDateFormat("yyyy-MM-dd");
                try {
                     registerDateTxt.setDate(sdf.parse(bus.getRegDate()));
                } catch (Exception e) {
                    e.printStackTrace();
                }
               
                registerBtn.setEnabled(false);
            }
            
        }
        }
        
        catch(Exception ex){
                
              ex.printStackTrace();
         }
    }//GEN-LAST:event_searchBtnActionPerformed


