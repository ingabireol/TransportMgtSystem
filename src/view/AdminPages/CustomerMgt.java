    private void addTableRowData(){
        tableModel.setRowCount(0);
        CustomerDao dao = new CustomerDao();
        List<Customer> customers = dao.retrieveCustomers();
        for(Customer thecustomers: customers){
            tableModel.addRow(new Object[]{
                thecustomers.getCust_id(),
                thecustomers.getCust_first_name(),
                thecustomers.getCust_last_name(),
                thecustomers.getCust_phone(),
                thecustomers.getGender()            
            });
        }
    }

