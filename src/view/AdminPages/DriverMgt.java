    private void registerBtnActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_registerBtnActionPerformed
        // TODO add your handling code here:
        if(fnameTxt.getText().trim().isEmpty() || lnameTxt.getText().trim().isEmpty() || salaryTxt.getText().trim().isEmpty() || licenceTxt.getText().trim().isEmpty() || busNoTxt.getText().trim().isEmpty() ||  phoneTxt.getText().trim().isEmpty() || genderCombo.getSelectedIndex() == 0 ){
            JOptionPane.showMessageDialog(this, "Plsease Fill All fields","Empty Field",JOptionPane.ERROR_MESSAGE);
            resetBtnActionPerformed(evt);
        }
        else if(!salaryTxt.getText().matches("\\d+(\\.\\d+)?") || busNoTxt.getText().length() != 5 || licenceTxt.getText().length() != 16 || !licenceTxt.getText().matches("\\d+")) {
            JOptionPane.showMessageDialog(this, "The salary is a Number \nDriverNo is of length 5\nAnd lecence is 16 digits","Empty Field",JOptionPane.ERROR_MESSAGE);
            resetBtnActionPerformed(evt);
        }
        else
        {
          Driver driver = new Driver();
          driver.setSalary(Double.parseDouble(salaryTxt.getText()));
          driver.setDriver_id(driverIdtxt.getText());
          driver.setF_name(fnameTxt.getText());
          driver.setL_name(lnameTxt.getText());
          driver.setGender(genderCombo.getSelectedItem().toString());
          driver.setLicence_no(licenceTxt.getText());
          driver.setContact_no(phoneTxt.getText());
          
          DriverDao driverdao = new DriverDao();
          if(driverdao.BusExists(busNoTxt.getText())){
          String feedback = driverdao.registerDriver(driver);
          JOptionPane.showMessageDialog(this, feedback, "Result", JOptionPane.INFORMATION_MESSAGE);
          addTableRowData();
          resetBtnActionPerformed(evt);
          }
          else{
              JOptionPane.showMessageDialog(this, "The BusNo("+busNoTxt.getText()+") Does not Exist", "Result", JOptionPane.ERROR_MESSAGE);
          }
        }
        
    }//GEN-LAST:event_registerBtnActionPerformed

