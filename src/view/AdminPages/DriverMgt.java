    private void searchBtnActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_searchBtnActionPerformed
        // TODO add your handling code here:
        try{
        if(searchIdtxt.getText().trim().isEmpty()){
             JOptionPane.showMessageDialog(this, "Please Enter Id ","Empty Filed",JOptionPane.ERROR_MESSAGE);
        }
        else if(searchIdtxt.getText().length() == 5 && searchIdtxt.getText().contains("DR")){
            Driver driver = new Driver();
//            Driver driver1 = new Driver();

            DriverDao jourdao = new DriverDao();
            driver.setDriver_id(searchIdtxt.getText());
            driver = jourdao.searchDriver(driver);
            if(driver == null){
                System.out.println(searchIdtxt.getText());
                JOptionPane.showMessageDialog(this, "Id does not Exist", "Result", JOptionPane.INFORMATION_MESSAGE);
            }
            else
            {
                driverIdtxt.setText(driver.getDriver_id());
                fnameTxt.setText(driver.getF_name());
                lnameTxt.setText(driver.getL_name());
                genderCombo.setSelectedItem(driver.getGender());
                phoneTxt.setText(driver.getContact_no());
                salaryTxt.setText(driver.getSalary().toString());
                licenceTxt.setText(driver.getLicence_no());
                busNoTxt.setText(driver.getBus_id());
                registerBtn.setEnabled(false);
            }
            
        }
        }
        
        catch(Exception ex){
                
              ex.printStackTrace();
         }
    }//GEN-LAST:event_searchBtnActionPerformed


